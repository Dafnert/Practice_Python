# Crea un programa que reciba un número del 1 al 4 y devuelva el día de la semana
# correspondiente: 1 lunes, 2 martes, 3 miércoles, 4 jueves
number = int(input("Pon un número del 1 al 4\n"))

if number ==1:
    print("1: Lunes")
elif number == 2: 
    print("2: Martes")
elif number == 3:
    print("3: Miércoles")
else: 
    print("4: Jueves")
